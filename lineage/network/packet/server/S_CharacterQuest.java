package lineage.network.packet.server;

import lineage.bean.lineage.Quest;
import lineage.network.packet.BasePacket;
import lineage.network.packet.Opcodes;
import lineage.network.packet.ServerBasePacket;
import lineage.world.controller.QuestController;
import lineage.world.object.instance.PcInstance;

public class S_CharacterQuest extends ServerBasePacket {

	static synchronized public BasePacket clone(BasePacket bp, PcInstance pc){
		if(bp == null)
			bp = new S_CharacterQuest(pc);
		else
			((S_CharacterQuest)bp).clone(pc);
		return bp;
	}
	
	public S_CharacterQuest(PcInstance pc){
		clone(pc);
	}
	
	public void clone(PcInstance pc){
		clear();

		writeC(Opcodes.S_OPCODE_UNKNOWN2);
		writeC(0xbc);
		for(int i=0 ; i<512 ; ++i) {
			String quest_name = String.format("Fairy GiftBox %d", i);
			Quest q = QuestController.find(pc, quest_name);
			if(q==null || q.getQuestStep()==0)
				writeC(0x00);
			else
				writeC(0x01);
		}
	}

}

