package lineage.bean.database;

public class RobotDrop {
	private String className;
	private String ItemName;
	private int ItemBress;
	private int ItemEnMin;
	private int ItemEnMax;
	private int CountMin;
	private int CountMax;
	private int Chance;
	public String getClassName() {
		return className;
	}
	public void setClassName(String className) {
		this.className = className;
	}
	public String getItemName() {
		return ItemName;
	}
	public void setItemName(String itemName) {
		ItemName = itemName;
	}
	public int getItemBress() {
		return ItemBress;
	}
	public void setItemBress(int itemBress) {
		ItemBress = itemBress;
	}
	public int getItemEnMin() {
		return ItemEnMin;
	}
	public void setItemEnMin(int itemEnMin) {
		ItemEnMin = itemEnMin;
	}
	public int getItemEnMax() {
		return ItemEnMax;
	}
	public void setItemEnMax(int itemEnMax) {
		ItemEnMax = itemEnMax;
	}
	public int getCountMin() {
		return CountMin;
	}
	public void setCountMin(int countMin) {
		CountMin = countMin;
	}
	public int getCountMax() {
		return CountMax;
	}
	public void setCountMax(int countMax) {
		CountMax = countMax;
	}
	public int getChance() {
		return Chance;
	}
	public void setChance(int chance) {
		Chance = chance;
	}

}
